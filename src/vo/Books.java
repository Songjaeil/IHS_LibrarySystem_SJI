package vo;

public class Books {
	private String BOOK_ID;
	private int BOOK_STOCK;
	private String BOOK_NAME;
	private int BOOK_PRICE;
	private int BOOK_RENTALPRICE;
	private String BOOK_AUTHOR;
	private String BOOK_PUBLISHER;

	public String getBOOK_ID() {
		return BOOK_ID;
	}

	public void setBOOK_ID(String bOOK_ID) {
		BOOK_ID = bOOK_ID;
	}

	public int getBOOK_STOCK() {
		return BOOK_STOCK;
	}

	public void setBOOK_STOCK(int bOOK_STOCK) {
		BOOK_STOCK = bOOK_STOCK;
	}

	public String getBOOK_NAME() {
		return BOOK_NAME;
	}

	public void setBOOK_NAME(String bOOK_NAME) {
		BOOK_NAME = bOOK_NAME;
	}

	public int getBOOK_PRICE() {
		return BOOK_PRICE;
	}

	public void setBOOK_PRICE(int bOOK_PRICE) {
		BOOK_PRICE = bOOK_PRICE;
	}

	public int getBOOK_RENTALPRICE() {
		return BOOK_RENTALPRICE;
	}

	public void setBOOK_RENTALPRICE(int bOOK_RENTALPRICE) {
		BOOK_RENTALPRICE = bOOK_RENTALPRICE;
	}

	public String getBOOK_AUTHOR() {
		return BOOK_AUTHOR;
	}

	public void setBOOK_AUTHOR(String bOOK_AUTHOR) {
		BOOK_AUTHOR = bOOK_AUTHOR;
	}

	public String getBOOK_PUBLISHER() {
		return BOOK_PUBLISHER;
	}

	public void setBOOK_PUBLISHER(String bOOK_PUBLISHER) {
		BOOK_PUBLISHER = bOOK_PUBLISHER;
	}

}
